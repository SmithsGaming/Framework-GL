    public final void setHex(String hexCode) {
        if (hexCode.length() != 7) {
            throw new IllegalArgumentException("Hex code must have 7 characters in the string #XXXXXX: " + hexCode);
        }
        int red = Integer.parseInt(hexCode.substring(1, 3), 16);
        int green = Integer.parseInt(hexCode.substring(3, 5), 16);
        int blue = Integer.parseInt(hexCode.substring(5, 7), 16);
        set(red / 255f, green / 255f, blue / 255f, 1f);
    }

